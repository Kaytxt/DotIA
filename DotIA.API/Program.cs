using Microsoft.EntityFrameworkCore;
using DotIA.API.Data;
using DotIA.API.Services;

var builder = WebApplication.CreateBuilder(args);

// ???????????????????????????????????????????????????????????
// CONFIGURAÇÃO DE SERVIÇOS
// ???????????????????????????????????????????????????????????

// ??? DATABASE ???????????????????????????????????????????????
builder.Services.AddDbContext<ApplicationDbContext>(options =>
    options.UseNpgsql(builder.Configuration.GetConnectionString("ConexaoDotIA")));

// ??? HTTP CLIENT & SERVIÇOS ??????????????????????????????????
builder.Services.AddHttpClient<IOpenAIService, OpenAIService>();
builder.Services.AddScoped<IOpenAIService, OpenAIService>();

// ??? CONTROLLERS & SWAGGER ???????????????????????????????????
builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new()
    {
        Title = "DotIA API",
        Version = "v1",
        Description = "API do sistema de suporte inteligente DotIA"
    });
});

// ??? CORS ????????????????????????????????????????????????????
builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll", policy =>
    {
        policy.AllowAnyOrigin()
              .AllowAnyMethod()
              .AllowAnyHeader();
    });
});

// ???????????????????????????????????????????????????????????
// CONFIGURAÇÃO DO MIDDLEWARE
// ???????????????????????????????????????????????????????????

var app = builder.Build();

// ??? DESENVOLVIMENTO ??????????????????????????????????????????
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI(c =>
    {
        c.SwaggerEndpoint("/swagger/v1/swagger.json", "DotIA API v1");
        c.RoutePrefix = "swagger";
    });
}

// ??? PRODUÇÃO ?????????????????????????????????????????????????
else
{
    app.UseExceptionHandler("/Error");
    app.UseHsts();
}

// ??? MIDDLEWARE PIPELINE ??????????????????????????????????????
app.UseHttpsRedirection();
app.UseCors("AllowAll");
app.UseAuthorization();
app.MapControllers();

// ???????????????????????????????????????????????????????????
// INICIALIZAÇÃO DO BANCO (CRIAR SE NÃO EXISTE)
// ???????????????????????????????????????????????????????????
using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<ApplicationDbContext>();
    try
    {
        db.Database.EnsureCreated();
        Console.WriteLine("? Banco de dados verificado/criado com sucesso!");
    }
    catch (Exception ex)
    {
        Console.WriteLine($"? Erro ao conectar no banco: {ex.Message}");
        Console.WriteLine("?? Verifique sua string de conexão no appsettings.json");
    }
}

// ???????????????????????????????????????????????????????????
// EXECUTAR APLICAÇÃO
// ???????????????????????????????????????????????????????????
Console.WriteLine("?? DotIA API iniciada!");
Console.WriteLine($"?? Swagger UI: https://localhost:5001/swagger");
Console.WriteLine($"?? API Base: https://localhost:5001/api");

app.Run();